using System;

namespace UD4_Ex8
{
    class Program
    {
        // 8) Recorre los números del 1 al 100. Usa un bucle while
        static void Main(string[] args)
        {
            int x = 0;

            while (x <= 100)
            {
                Console.WriteLine(x);
                x++;
            }
        }
    }
}
