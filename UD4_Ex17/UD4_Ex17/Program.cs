using System;

namespace UD4_Ex17
{
    //17) Muestra los números del 1 al 100 (ambos incluidos). Usa un bucle while
    class Program
    {
        static void Main(string[] args)
        {
            int x = 0; 

            while (x <= 100)
            {
                Console.WriteLine(x);
                x++;
            }
        }
    }
}
